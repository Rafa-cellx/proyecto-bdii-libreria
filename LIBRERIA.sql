--TABLA DIRECCIONES-[*]
CREATE TABLE DIRECCIONES_CRAM(
    ID_DIRECCION NUMBER(6) CONSTRAINT DIR_CRAM_PK PRIMARY KEY,
    NUMERO_EDIFICIO NUMBER(5),
    NUMERO_CALLE NUMBER(5) CONSTRAINT DIR_CRAM_NUM_CAL_NN NOT NULL,
    AREA_LOCALIDAD VARCHAR2(100),
    CIUDAD VARCHAR2(50) CONSTRAINT DIR_CRAM_CIU_NN NOT NULL,
    CODIGO_POSTAL NUMBER(6),
    ESTADO VARCHAR2(50),
    PAIS VARCHAR2(50) CONSTRAINT DIR_CRAM_PAI_NN NOT NULL,
    OTROS_DETALLES VARCHAR2(150)
);

--TABLA CATEGORIAS-[*]
CREATE TABLE CATEGORIAS_CRAM(
    ID_CATEGORIA NUMBER(6) CONSTRAINT CAT_CRAM_PK PRIMARY KEY,
    NOMBRE_CATEGORIA VARCHAR2(40),
    DESCRIPCION VARCHAR2(200)
);

--TABLA EDITORIALES-[*]
CREATE TABLE EDITORIALES_CRAM(
    ID_EDITORIAL NUMBER(6) CONSTRAINT EDIT_CRAM_PK PRIMARY KEY,
    NOMBRE_EDITORIAL VARCHAR2(50) CONSTRAINT EDIT_CRAM_NOM_NN NOT NULL,
    PAIS VARCHAR2(40),
    SITIO_WEB VARCHAR2(150)
);

--TABLA AUTORES-[*]
CREATE TABLE AUTORES_CRAM(
    ID_AUTOR NUMBER(6) CONSTRAINT AUT_CRAM_PK PRIMARY KEY,
    NOMBRE VARCHAR2(50) CONSTRAINT AUT_CRAM_NOM_NN NOT NULL,
    APELLIDO VARCHAR2(50) CONSTRAINT AUT_CRAM_APE_NN NOT NULL
);

